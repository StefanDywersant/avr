// expanded key
static uint8_t key[128];

// position of last expanded key read
static uint8_t key_pos;


void mix_up(uint16_t* block) {
	uint8_t i;

	for (i = 0; i < 4; i++) {
		block[i] = (uint16_t)(
				block[i]
				+ ((uint16_t*)key)[key_pos]
				+ (block[MOD(i - 1, 4)] & block[MOD(i - 2, 4)])
				+ ((~block[MOD(i - 1, 4)]) & block[MOD(i - 3, 4)]));

		key_pos = (key_pos + 1) % 64;

		if (i == 3)
			block[i] = SHIFT_L(block[i], 5);
		else
			block[i] = SHIFT_L(block[i], i + 1);
	}
}


void r_mix_up(uint16_t* block) {
	uint8_t i;

	for (i = 3; i != 255; i--) {
		if (i == 3)
			block[i] = SHIFT_R(block[i], 5);
		else
			block[i] = SHIFT_R(block[i], i + 1);

		block[i] = (uint16_t)(
				block[i]
				- ((uint16_t*)key)[key_pos]
				- (block[MOD(i - 1, 4)] & block[MOD(i - 2, 4)])
				- ((~block[MOD(i - 1, 4)]) & block[MOD(i - 3, 4)]));

		key_pos = (uint8_t)(key_pos - 1) % 64;
	}
}


void mash_up(uint16_t* block) {
	uint8_t i;

	for (i = 0; i < 4; i++)
		block[i] = block[i] + ((uint16_t*)key)[block[MOD(i - 1, 4)] & 63];
}


void r_mash_up(uint16_t* block) {
	uint8_t i;

	for (i = 3; i != 255; i--)
		block[i] = block[i] - ((uint16_t*)key)[block[MOD(i - 1, 4)] & 63];
}


static void encrypt_block(uint8_t* block) {
	uint8_t i;

	key_pos = 0;

	// 5 x mix up
	for (i = 0; i < 5; i++)
		mix_up((uint16_t*)block);

	// 1 x mash up
	mash_up((uint16_t*)block);

	// 6 x mix up
	for (i = 0; i < 6; i++)
		mix_up((uint16_t*)block);

	// 1 x mash up
	mash_up((uint16_t*)block);

	// 5 x mix up
	for (i = 0; i < 5; i++)
		mix_up((uint16_t*)block);
}


static void decrypt_block(uint8_t* block) {
	uint8_t i;

	key_pos = 63;

	// 5 x mixup
	for (i = 0; i < 5; i++)
		r_mix_up((uint16_t*)block);

	// 1 x mashup
	r_mash_up((uint16_t*)block);

	// 6 x mixup
	for (i = 0; i < 6; i++)
		r_mix_up((uint16_t*)block);

	// 1 x mashup
	r_mash_up((uint16_t*)block);

	// 5 x mixup
	for (i = 0; i < 5; i++)
		r_mix_up((uint16_t*)block);
}


void rc2_set_key(uint8_t* k) {
	uint8_t i;

	// fill in beginning of expanded key
	for (i = 0; i < 8; i++)
		key[i] = k[i];

	// step 1
	for (i = 8; i < 128; i++)
		key[i] = pgm_read_byte(&pi[(uint8_t)(key[i - 1] + key[i - 8])]);

	// step 2
	key[120] = pgm_read_byte(&pi[key[120]]);

	// step 3
	for (i = 119; i != 255; i--)
		key[i] = pgm_read_byte(&pi[key[i + 1] ^ key[i + 8]]);
}


void rc2_encrypt(uint8_t len, uint8_t* payload) {
	uint8_t i;

	// encrypt each of 64bit blocks
	for (i = 0; i < (uint8_t)(len / 8); i++)
		encrypt_block((uint8_t*)&payload[i * 8]);
}


void rc2_decrypt(uint8_t len, uint8_t* payload) {
	uint8_t i;

	// decrypt each of 64bit blocks
	for (i = 0; i < (uint8_t)(len / 8); i++)
		decrypt_block((uint8_t*)&payload[i * 8]);
}
